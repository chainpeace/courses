void onebit_HuffmanDecoder(void)
{
	int i=0, o=0, j, l;
    int k;
	unsigned char current_val, bit_cnt=0;
	unsigned int bit_seq=0;
	unsigned int idx = 0, hit = 0;
	
	while(i<sizeof(inputBitStream))
    {
		current_val = inputBitStream[i++];
		for(j=0; j<8; j++) 
        {
			if(current_val & 0x080){
				bit_cnt++;
				bit_seq = bit_seq << 1; 
				bit_seq = bit_seq | 0x01;		
			}
			else{
				bit_cnt++;
				bit_seq = bit_seq << 1;
			}
            current_val = current_val << 1;
            
            /* implement here */
			if(bit_cnt < 2)
				continue;

			// end of input stream
			if(bit_cnt > 18)
				return;
			
			//19 > cnt >= 2 from here
			for(idx;idx<NUM_OF_SYMBOLS;idx++){
				// not in this length
				if(sym_tab[idx].codeword_len != bit_cnt)
					break;
				else{
					// hit
					if(sym_tab[idx].codeword == bit_seq){
						outputResult[o++] = sym_tab[idx].symbol;
						hit = 1;
						break;
					}
				}

			}
			//initialize 
			if(hit){
				idx = 0;
				bit_cnt = 0;
				bit_seq = 0;
				hit = 0;
			}
        }
    }
}


